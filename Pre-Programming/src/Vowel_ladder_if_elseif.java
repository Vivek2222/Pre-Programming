public class Vowel_ladder_if_elseif 
{
		public static void main(String[]args)
		{
			 char ch = 'D';
			 if (ch=='a'||ch=='e'||ch=='i'||ch=='o'||ch=='u'||ch=='A'||ch=='E'||ch=='I'||ch=='O'||ch=='U')
			 {
			  System.out.println(ch +" Vowel ");
			 }
			 else
			 {
			  System.out.println(ch +" Consonant ");
			 }	
		 }
}
